// Tipos de dados
let x = 10,
    y = 2;

// Somar
let resultado = x + y;
console.log(resultado);

// Subtrair
resultado = x - y;
console.log(resultado);

// Multiplicar
resultado = x * y;
console.log(resultado);

// Dividir
resultado = x / y;
console.log(resultado);

// Exponenciar 
resultado = x ** y;
console.log(resultado);

// Raiz
resultado = x ** (1 / y);
console.log(resultado);